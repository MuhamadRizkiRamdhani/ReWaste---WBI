def clean_data(df):
    # Replace missing values with the mean of each column in: 'SISA'
    df = df.fillna({'SISA': df['SISA'].mean()})
    # Filter rows based on column: 'SISA'
    df = df[(df['SISA'] > -26.73) & (df['SISA'] < 27.7)]
    # Replace missing values with the mean of each column in: 'DIOLAH'
    df = df.fillna({'DIOLAH': df['DIOLAH'].mean()})
    # Replace missing values with the mean of each column in: 'ANGKUT'
    df = df.fillna({'ANGKUT': df['ANGKUT'].mean()})
    # Replace missing values with the mean of each column in: 'INPUT'
    df = df.fillna({'INPUT': df['INPUT'].mean()})
    # Drop rows with missing data across all columns
    df = df.dropna()
    return df

