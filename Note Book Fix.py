from sklearn.preprocessing import MinMaxScaler
from sklearn.ensemble import RandomForestClassifier
from sklearn.model_selection import StratifiedKFold, cross_validate, train_test_split
from sklearn.metrics import classification_report, confusion_matrix, ConfusionMatrixDisplay
# Fitur model: wbi_tps TIDAK dimasukkan untuk mencegah label bug
X_feat = ['rasio_angkut', 'rasio_diolah', 'rasio_sisa', 'indeks_jarak']
X = df_tps_feat[X_feat].values
y = df_tps_feat['LABEL'].values

# Split Train/Test 80:20 — data test tidak pernah dilihat model saat training
X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.2, stratify=y, random_state=42
)
print(f"Ukuran data  — Train: {len(X_train)}, Test: {len(X_test)}")

# Cross-Validation 5-fold pada data training
model = RandomForestClassifier(n_estimators=100, random_state=42, class_weight='balanced')
cv    = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

cv_results = cross_validate(
    model, X_train, y_train, cv=cv,
    scoring=['f1_weighted', 'accuracy'],
    return_train_score=True
)

print(f"\nCross-Validation (5-fold) pada Data Training:")
print(f"  Train Accuracy : {cv_results['train_accuracy'].mean():.4f} ± {cv_results['train_accuracy'].std():.4f}")
print(f"  Val   Accuracy : {cv_results['test_accuracy'].mean():.4f}  ± {cv_results['test_accuracy'].std():.4f}")
print(f"  Train F1-Score : {cv_results['train_f1_weighted'].mean():.4f} ± {cv_results['train_f1_weighted'].std():.4f}")
print(f"  Val   F1-Score : {cv_results['test_f1_weighted'].mean():.4f}  ± {cv_results['test_f1_weighted'].std():.4f}")

gap_acc = cv_results['train_accuracy'].mean() - cv_results['test_accuracy'].mean()
gap_f1  = cv_results['train_f1_weighted'].mean() - cv_results['test_f1_weighted'].mean()
print(f"\n  Gap Accuracy (Train−Val) : {gap_acc:.4f}")
print(f"  Gap F1-Score (Train−Val) : {gap_f1:.4f}")
if gap_acc > 0.10:
    print("  ⚠️  Gap > 0.10 — indikasi overfitting")
else:
    print("  ✅  Gap dalam batas wajar — model generalisasi baik")

# Fit final model & evaluasi pada Test Set
model.fit(X_train, y_train)
y_pred = model.predict(X_test)

print(f"\nEvaluasi pada Test Set (data yang tidak pernah dilihat model):")
print(classification_report(y_test, y_pred, zero_division=0))

feat_imp = pd.Series(model.feature_importances_, index=X_feat).sort_values(ascending=False)
print(f"Feature Importance:\n{feat_imp.round(4).to_string()}")


