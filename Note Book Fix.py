# Normalisasi fitur ke rentang [0,1] sebelum pembobotan
from sklearn.preprocessing import MinMaxScaler
scaler = MinMaxScaler()
df['sampah_per_kapita_norm'] = scaler.fit_transform(df[['sampah_per_kapita']])
df['beban_per_tps_norm']     = scaler.fit_transform(df[['beban_per_tps']])
df['rasio_sisa_norm']        = scaler.fit_transform(df[['rasio_sisa']])

# WBI: skor komposit — semakin tinggi = semakin berat beban pengelolaan
# Bobot: rasio_sisa(0.35) + sampah_per_kapita(0.25) + indeks_jarak(0.20) + beban_per_tps(0.20)
df['WBI'] = (
    0.35 * df['rasio_sisa_norm'] +
    0.25 * df['sampah_per_kapita_norm'] +
    0.20 * df['indeks_jarak'] +
    0.20 * df['beban_per_tps_norm']
)
df.fillna(0, inplace=True)

print(df[['SWK', 'WBI']].sort_values('WBI', ascending=False).round(4).to_string(index=False))


